package ru.isa.ai.dhm;

public class RegionSettingsException extends Exception {
        public RegionSettingsException() {
            super();
        }

        public RegionSettingsException(String message) {
            super(message);
        }

        public RegionSettingsException(String message, Throwable cause) {
            super(message, cause);
        }

        public RegionSettingsException(Throwable cause) {
            super(cause);
        }
}
