package ru.isa.ai.linguistic;

public class LinguisticsAnalizingException extends Exception {
	private static final long serialVersionUID = -1746765698641972017L;

	public LinguisticsAnalizingException() {
		super();
	}

	public LinguisticsAnalizingException(String message) {
		super(message);
	}

	public LinguisticsAnalizingException(String message, Throwable cause) {
		super(message, cause);
	}

	public LinguisticsAnalizingException(Throwable cause) {
		super(cause);
	}
}
